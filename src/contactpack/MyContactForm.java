    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed

        String name = jTextField_Name.getText();
        String email = jTextField_Email.getText();
        String phone = jTextField_Phone.getText();
        String address = jTextAreaAddress.getText();
        String groupc = jComboBoxGroup.getSelectedItem().toString();
        byte[] img = null;
        // to check if the fields are empty
        if(imagePth != null){
        try {
            
            Path pth = Paths.get(imagePth);
            img = Files.readAllBytes(pth);
            contact c = new contact(null, name, email, phone, address, groupc, img, currentUserId);
            contactQuery cq = new contactQuery();
            cq.insertContact(c);
            refreshJtable();
            clearFields();
            
        } catch (IOException ex) {
            Logger.getLogger(MyContactForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        } else {
            JOptionPane.showMessageDialog(null, "You Must Select a Profile Picture");
        }
        
        
    }//GEN-LAST:event_jButtonAddActionPerformed

    public void refreshJtable(){
        
        jTable1.setModel(new DefaultTableModel());
        populateJtable();
        
    }
    
    public void clearFields(){
        
        jTextField_Id.setText("");
        jTextField_Name.setText("");
        jTextField_Email.setText("");
        jTextField_Phone.setText("");
        jComboBoxGroup.setSelectedIndex(0);
        jLabelPic.setIcon(null);
        jTextAreaAddress.setText("");
        // set imagePth to null to prevent the user to add unwanted duplicate images
        imagePth = null;
        
    }
    
    private void showData(int index){
        
        jTextField_Id.setText(jTable1.getValueAt(index, 0).toString());
        jTextField_Name.setText(jTable1.getValueAt(index, 1).toString());
        jTextField_Email.setText(jTable1.getValueAt(index, 2).toString());
        jTextField_Phone.setText(jTable1.getValueAt(index, 3).toString());
        jTextAreaAddress.setText(jTable1.getValueAt(index, 4).toString());
        // Group
        jComboBoxGroup.setSelectedItem(jTable1.getValueAt(index, 5));
        // Pic
        Image pic = ((ImageIcon)jTable1.getValueAt(index, 6)).getImage().getScaledInstance(jLabelPic.getWidth(), jLabelPic.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon img = new ImageIcon(pic);
        
        jLabelPic.setIcon(img);
        
    }
    
