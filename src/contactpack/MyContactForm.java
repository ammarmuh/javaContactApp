    public void populateJtable(){
        contactQuery cq = new contactQuery();
        ArrayList<contact> ctList = cq.contactList(currentUserId);
        // groupc, phone, email, address, pic
        String[] colNames = {"Id","Name","Email","Phone","Address","Group","Image"};
        Object[][] rows = new Object[ctList.size()][7];
        
        for(int i = 0; i < ctList.size(); i++){
            rows[i][0] = ctList.get(i).getCid();
            rows[i][1] = ctList.get(i).getName();
            rows[i][2] = ctList.get(i).getEmail();
            rows[i][3] = ctList.get(i).getPhone();
            rows[i][4] = ctList.get(i).getGroupc();
            rows[i][5] = ctList.get(i).getAddress();
            
            ImageIcon pic = new ImageIcon(new ImageIcon
                                         (ctList.get(i).getPic())
                                                 .getImage()
                                                 .getScaledInstance(100, 100, Image.SCALE_SMOOTH));
            
            rows[i][6] = pic;
        }
        
        MyModel mmd = new MyModel(rows, colNames);
        jTable1.setModel(mmd);
        jTable1.setRowHeight(80);
        jTable1.getColumnModel().getColumn(6).setPreferredWidth(100);
        
    }
    
    
