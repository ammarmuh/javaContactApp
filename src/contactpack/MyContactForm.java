    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        
        // the user must select a contact to edit
        if(!jTextField_Id.getText().equals("")){
            int id = Integer.valueOf(jTextField_Id.getText());
            String name = jTextField_Name.getText();
            String email = jTextField_Email.getText();
            String phone = jTextField_Phone.getText();
            String address = jTextAreaAddress.getText();
            String groupc = jComboBoxGroup.getSelectedItem().toString();
        
        // if the user want to update the data and the image
        if(imagePth != null){
            byte[] img = null;
            try {

                Path pth = Paths.get(imagePth);
                img = Files.readAllBytes(pth);
                
                contact c = new contact(id, name, email, phone, address, groupc, img, currentUserId);
                contactQuery cq = new contactQuery();
                cq.updateContact(c, true);
                refreshJtable();
                clearFields();

            } catch (IOException ex) {
                Logger.getLogger(MyContactForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
                contact c = new contact(id, name, email, phone, address, groupc, null, currentUserId);
                contactQuery cq = new contactQuery();
                cq.updateContact(c, false);
                refreshJtable();
                clearFields();
            
            }
        } else {
            JOptionPane.showMessageDialog(null, "Select a Contact From Table");
            }
        
        
    }//GEN-LAST:event_jButtonEditActionPerformed

